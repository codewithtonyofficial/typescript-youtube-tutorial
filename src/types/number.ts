let myNumber: number = 42;

// what we can do with a number type
myNumber = 100;

myNumber = 32.4;

myNumber = -150;

myNumber = 2 + 2;
myNumber = 2 - 2;
myNumber = 2 * 2;
myNumber = Infinity * 0.12;

myNumber = parseInt("42");

// what we cannot do with a number type
myNumber = "42";

myNumber.toUperCase();
